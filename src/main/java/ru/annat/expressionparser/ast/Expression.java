package ru.annat.expressionparser.ast;
import java.util.Map;

/**
 * Базовый интерфейс для всех узлов абстрактного синтаксического дерева выражения.
 *
 * <p>Каждая реализация представляет отдельный вид выражения
 * и должна уметь вычислять своё значение на основе переданных переменных.</p>
 */
public interface Expression {

    /**
     * Вычисляет значение выражения.
     *
     * @param variables таблица значений переменных, где ключ — имя переменной,
     *                  значение — её числовое значение
     * @return результат вычисления выражения
     */
    double evaluate(Map<String, Double> variables);
}